#Definition of TELOS

echo -e "TELOS are the revelant factors that need to be taken into account when
running a feasibility study:
\nT - technology (technical feasibility): Is the existing technology sufficient
to implement the proposed system?  
\nE - economy (economical feasibility): Is the proposed system cost effective?
\nL - legality (legal feasibility): Are there any conflicts between the proposed
system and any regulations/ laws?
\nO - operation (operational feasibility): Are the existing organizational 
practices and procedures sufficient to support the maintenance and operation
of the new system?   
\nS - schedule (schedule feasibility): How long will we wait?"
